"use strict";
var z1 = 15;
var z2 = 7;
var erg;
// Addition
erg = z1 + z2;
console.log(erg);
document.write(erg + "<br />");
// Division
erg = z1 / z2;
console.log(erg);
document.write(erg + "<br />");
// Multiplikation
erg = z1 * z2;
console.log(erg);
document.write(erg + "<br />");
// Subtraktion
erg = z1 - z2;
console.log(erg);
document.write(erg + "<br />");
// Modulo
erg = z1 % z2;
console.log(erg);
document.write(erg + "<br />");
erg = 7.1 % 3.4;
console.log(erg);
document.write(erg + "<br />");
// Negierung und Plusvorzeichen
console.log(-z1);
document.write(-z1 + "<br />");
console.log(+z1);
document.write(+z1 + "<br />");
// Inkrement
erg = z1;
console.log(erg++);
erg = z1;
console.log(erg++);
document.write(erg + "<br />");
erg = z1;
console.log(++erg);
erg = z1;
console.log(++erg);
document.write(erg + "<br />");
// Dekrement
erg = z1;
console.log(--erg);