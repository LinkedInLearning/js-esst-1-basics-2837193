"use strict";
var z1 = 5;
var z2 = 2;

// Und bit
console.log(true & true);
console.log(true & false);

// Oder bit
console.log(false | true);
console.log(false | false);

// Exklusiv Oder (XOR - bit)
console.log(true ^ true);
console.log(true ^ false);

// Verschiebung
console.log(z1 >> 1);
console.log(z2 >> 1);
console.log(z1 << 1);